package com.harin.springpracticeone.game;

public class MarioGame {

	public void up() {
		System.out.println("Mario Jump");
	}

	public void down() {
		System.out.println("Mario go to hole");
	}
	
	public void left() {
		System.out.println("Mario go back");
	}
	
	public void right() {
		System.out.println("Mario go forward");
	}
}
